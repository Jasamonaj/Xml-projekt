<h3>O nama</h3>
    
    <hr>
        
    <h5>
Max Emilian Verstappen (rođen 30. rujna 1997.) je belgijsko-nizozemski vozač utrka i svjetski prvak Formule 1 2021. i 2022. Natječe se pod nizozemskom zastavom u Formuli 1 s Red Bull Racingom.

Verstappen je sin trkaćih vozača Josa Verstappena, koji se također natjecao u Formuli 1, i Sophie Kumpen. Imao je uspješnu vožnju u juniorskim karting kategorijama i kategorijama jednosjeda – uključujući KF3, 
    Svjetsku seriju WSK, KZ2 i europsku Formulu 3 – oborivši nekoliko rekorda.

Na Velikoj nagradi Australije 2015., kada je imao 17 godina i 166 dana, postao je najmlađi vozač koji se natjecao u Formuli 1. Nakon što je sezonu 2015. proveo u Scuderiji Toro Rosso, Verstappen je započeo svoju kampanju 2016. 
s talijanskom momčadi prije nego što je nakon četiri utrke promoviran u matičnu momčad Red Bull Racing kao zamjena za Daniila Kvyata. U dobi od 18 godina osvojio je Veliku nagradu Španjolske 2016. u svom debiju za Red Bull Racing, 
postavši najmlađi vozač i prvi nizozemski vozač koji je osvojio Veliku nagradu Formule 1. Nakon pobjede na Velikoj nagradi Abu Dhabija 2021., Verstappen je postao prvi nizozemski vozač koji je osvojio Svjetsko prvenstvo 
Formule 1 i 34. svjetski prvak u Formuli 1 među vozačima. Sljedeće sezone osvojio je svoju drugu uzastopnu titulu prvaka Formule 1.

Od VN Abu Dhabija 2022. Verstappen je ostvario 35 pobjeda i 20 pole positiona. Postigao je prvi hat-trick u karijeri na Velikoj nagradi Francuske 2021. i svoj prvi grand slam na Velikoj nagradi Austrije 2021.. 
Osvojio je svoj drugi Grand Slam na Velikoj nagradi Emilije Romagne 2022. Verstappen bi trebao ostati u Red Bullu barem do kraja sezone 2028. nakon što je potpisao produljenje ugovora.
    </h5>
    
    <h5>
Max Emilian Verstappen rođen je 30. rujna 1997. u Hasseltu u Belgiji od roditelja Josa Verstappena i Sophie Kumpen, a ima i mlađu sestru Victoriju.
 Verstappenovi roditelji razišli su se kad je bio mlad, nakon čega je on živio s ocem, dok je njegova sestra Victoria živjela s njihovom majkom.
 Verstappen ima tri mlađa polubraće i sestre od svog oca; sestra, Blue Jaye, iz drugog braka i brat i sestra, Jason Jaxx i Mila Faye, iz sadašnjeg braka.

Njegova obitelj ima dugu vezu s motornim sportovima: njegov otac je nizozemski bivši vozač Formule 1, njegova majka Belgijanka natjecala se u kartingu, 
a njegov prvi rođak, Anthony Kumpen, natjecao se u utrkama izdržljivosti i dvostruki prvak NASCAR Whelen Euro Series trenutno radi kao menadžer tima za PK Carsport u Euro Series.
 Iako Verstappen ima belgijsku majku, rođen je u Belgiji i živi u belgijskom Breeju, odlučio se natjecati s nizozemskom trkaćom licencom jer se "osjeća više Nizozemcem", 
budući da je zbog kartinga više vremena provodio s ocem nego s majkom aktivnostima, i uvijek je bila okružena Nizozemcima dok je odrastala u Maaseiku, belgijskom gradu na nizozemskoj granici.
Verstappen je 2015. rekao: "U Belgiji sam zapravo živio samo da bih spavao, ali preko dana sam otišao u Nizozemsku i tamo sam imao svoje prijatelje. Odgojen sam kao Nizozemac i tako se osjećam."

Natjecao se u Formuli 1 više od jedne sezone prije nego što je dobio dozvolu za cestovnu vožnju na svoj 18. rođendan. Verstappen se preselio u Monako dan poslije, u listopadu 2015., i od tada tamo 
živi i rekao je da to nije zbog poreznih razloga.U studenom 2020. Verstappen je kupio zrakoplov Dassault Falcon 900EX od Virgin Galactica. Zrakoplov je registriran PH-DTF, a njime upravlja Exxaero.

U slobodno vrijeme, Verstappen uživa u simultanim utrkama. Nakon što je sudjelovao u mnogim natjecanjima simulacijskih utrka, Verstappen je izjavio da mu to pomaže u utrkama u stvarnom životu držeći ga ″spremnim za polazak″.
 Verstappen je član Team Redlinea; multinacionalni simulacijski tim. Verstappen navija za FC Barcelonu i PSV Eindhoven. Verstappen govori tri jezika, nizozemski, engleski i njemački, 
od kojih je posljednji naučio dok je vozio karting s Michaelom Schumacherom. U rujnu 2022. proglašen je časnikom Reda Orange-Nassau kao priznanje za njegova trkačka postignuća.
 Verstappen je javno izlazio s Kelly Piquet od siječnja 2021.
    </h5>

